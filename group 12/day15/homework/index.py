
score = float(input("შეიტანეთ თქვენი ქულა: "))

if 90 < score <= 100:
    print("შენ შეგიძლია ისწავლო უფასოდ ")
elif 70 < score < 90:
    print("შენ უნდა გადაიხადო 1500 ლარი")
elif 40 < score <= 70:
    print("შენ უნდა გადაიხადო მარტო 500 ლარი")
else:
    print("შენ არშეგიძლია აქ სწავლა რადგან დაბალი შეფასება გაქვს")
