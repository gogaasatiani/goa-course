
let sum = 0;

for (let i = 1; i <= 100; i++) {
    sum += i;
}
console.log(`The sum of numbers from 1 to 100 is: ${sum}`);





let num = 5;

for (let i = 1; i <= 10; i++) {
    console.log(`${num} x ${i} = ${num * i}`);
}


let str = "javascript is fun";

let char = 'a';

let count = 0;

for (let i = 0; i < str.length; i++) {
    if (str[i] === char) {
        count++;
    }
}
console.log(`The character '${char}' appears ${count} times in the string.`);
























































